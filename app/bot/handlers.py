"""
Telegram bot message handlers
"""
from datetime import datetime
import re


class MessageHandlers:
    """Обработчики сообщений для Telegram бота"""

    @staticmethod
    def start_handler() -> str:
        """Обработчик команды /start"""
        return (
            "🌟 *Добро пожаловать в Трекер Эмоций!* 🌟\n\n"
            "Я буду напоминать вам о необходимости отмечать свои эмоции.\n\n"
            "📝 *Как это работает:*\n"
            "• Ежедневно я буду присылать вам напоминание\n"
            "• Вы переходите по ссылке и заполняете короткий опрос\n"
            "• Ваши ответы помогут отслеживать эмоциональное состояние\n\n"
            "✅ *Готово!* Теперь вы будете получать ежедневные напоминания.\n\n"
            "⚙️ *Команды:*\n"
            "/start - Приветственное сообщение\n"
            "/help - Помощь\n"
            "/settings - Настройки уведомлений\n"
            "/stop - Отписаться от уведомлений"
        )

    @staticmethod
    def help_handler() -> str:
        """Обработчик команды /help"""
        return (
            "🆘 *Помощь*\n\n"
            "📋 *Команды:*\n"
            "/start - Начать работу с ботом\n"
            "/help - Показать это сообщение\n"
            "/settings - Настроить время уведомлений\n"
            "/stop - Отключить уведомления\n\n"
            "❓ *Вопросы?*\n"
            "Если у вас возникли проблемы, напишите нам: support@emotions-tracker.com"
        )

    @staticmethod
    def settings_handler(reminder_time: str = "20:00") -> str:
        """Обработчик команды /settings"""
        return (
            "⚙️ *Настройки уведомлений*\n\n"
            f"🕐 Текущее время напоминания: *{reminder_time}*\n\n"
            "Чтобы изменить время, отправьте команду:\n"
            "/set_time HH:MM\n\n"
            "Пример: `/set_time 21:30`"
        )

    @staticmethod
    def set_time_handler(time_str: str) -> str:
        """Обработчик установки времени"""
        if not re.match(r'^([01]\d|2[0-3]):([0-5]\d)$', time_str):
            return "❌ Неверный формат времени. Используйте HH:MM (например, 21:30)"

        return f"✅ Время уведомлений успешно изменено на *{time_str}*"

    @staticmethod
    def stop_handler() -> str:
        """Обработчик команды /stop"""
        return (
            "👋 *До свидания!*\n\n"
            "Вы отписаны от ежедневных напоминаний.\n\n"
            "Чтобы снова получать уведомления, отправьте команду /start"
        )

    @staticmethod
    def reminder_message(survey_url: str) -> str:
        """Сообщение-напоминание"""
        return (
            "🌅 *Ежедневное напоминание*\n\n"
            "Как вы себя чувствуете сегодня?\n"
            "Пожалуйста, уделите 2 минуты, чтобы отметить свои эмоции.\n\n"
            f"📝 [Пройти опрос]({survey_url})\n\n"
            "_Это поможет вам лучше понимать свое эмоциональное состояние._"
        )

    @staticmethod
    def unknown_handler() -> str:
        """Обработчик неизвестной команды"""
        return "❓ Неизвестная команда. Используйте /help для списка доступных команд."